    private void CEQUALActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CEQUALActionPerformed
        String answer;
        secondnum=Double.parseDouble(caldisplay.getText());
        if(operation=="+"){
            result=firstnum+secondnum;
            answer=String.format("%.2f", result);
            caldisplay.setText(answer);
        }
        else if(operation=="-"){
            result=firstnum-secondnum;
            answer=String.format("%2f", result);
            caldisplay.setText(answer);
        }
        else if(operation=="*"){
            result=firstnum*secondnum;
            answer=String.format("%2f", result);
            caldisplay.setText(answer);
        }
        else if(operation=="/"){
            result=firstnum/secondnum;
            answer=String.format("%2f", result);
            caldisplay.setText(answer);
        }
        else if(operation=="%"){
            result=(firstnum*secondnum)/100;
            answer=String.format("%2f", result);
            caldisplay.setText(answer);
        }
    }//GEN-LAST:event_CEQUALActionPerformed

